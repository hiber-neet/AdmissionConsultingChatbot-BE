            confidence = result.get("confidence", 0.0)

            # === TIER 1: training_qa - score > 0.8 ===
            if tier_source == "training_qa" and confidence > 0.7:
                print("floor 1")
                top = result["top_match"]
                q_text = top.payload.get("question_text")
                a_text = top.payload.get("answer_text")
